# FinanceDataReader StockListing("KOSDAQ")의 Marcap 기준 상위 250 (스냅샷, FDR 기준일 데이터).
# 갱신 시: pip install finance-datareader 후 아래 로직으로 dict를 다시 만들어 이 파일의
# KOSDAQ_TOP250_MCAP 본문만 교체하면 됩니다.
#   import FinanceDataReader as fdr
#   d = fdr.StockListing("KOSDAQ").sort_values("Marcap", ascending=False).head(250)

def _infer_kosdaq_sector(name: str) -> str:
    """시총상위 보조 유니버스용 섹터 라벨(키워드 기반 휴리스틱)."""
    n = str(name or "").lower()

    if any(k in n for k in ("로보", "robot", "메카", "자동화")):
        return "로봇/신성장"
    if any(k in n for k in ("바이오", "bio", "pharm", "제약", "테라", "헬스", "메디", "진단", "유전자")):
        return "바이오/의료"
    if any(k in n for k in ("에코프로", "전지", "배터리", "리튬", "양극", "음극", "소재", "머티리얼", "켐", "엔켐")):
        return "2차전지/소재"
    if any(k in n for k in ("반도체", "sem", "칩", "실리콘", "테스", "테크", "ips", "hpsp", "qnc", "네오룩스")):
        return "반도체/AI"
    if any(k in n for k in ("게임", "ent", "엔터", "스튜디오", "드래곤", "카카오", "위메이드", "soop", "디어유")):
        return "콘텐츠/서비스"
    if any(k in n for k in ("통신", "광통신", "kcp", "보안", "안랩", "kmw", "솔루션")):
        return "통신/네트워크"
    if any(k in n for k in ("전기", "파워", "에너지", "기계", "장비", "시스템", "벤드", "테크놀로지")):
        return "기계/전력인프라"
    return "기타/성장"


